// % Resto da divisão
const n1 = 5;
const n2 = 10;

console.log(n1 + n2);
console.log(n1 - n2);
console.log(n1 * n2);
console.log(n1 / n2);
console.log(2 ** 10); // Potenciação
console.log(n1 % n2);

