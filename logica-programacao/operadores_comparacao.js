/**
 * OPERADORES DE COMPARAÇÃO
 * > maior que
 * >= maior que ou igual a
 * < menor que
 * <= menor que ou igual a
 * == igualdade
 * === igualdade estrit
 * != diferente
 * !== diferença estrita
 */

console.log(10 > 5);
console.log(10 >= 10);
console.log(10 < 10);
console.log(10 <= 10);
console.log(10 == 10);
console.log(10 == "10");
console.log(10 === 10);
console.log(10 === "10");
